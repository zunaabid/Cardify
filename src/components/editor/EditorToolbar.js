"use client"
import "./EditorToolbar.css"

function EditorToolbar({ zoom, onZoomChange, selectedElement, onDelete, onDuplicate }) {
  const handleZoomIn = () => {
    onZoomChange(Math.min(200, zoom + 10))
  }

  const handleZoomOut = () => {
    onZoomChange(Math.max(50, zoom - 10))
  }

  const handleZoomReset = () => {
    onZoomChange(100)
  }

  return (
    <div className="editor-toolbar">
      <div className="toolbar-section zoom-controls">
        <button className="toolbar-button" onClick={handleZoomOut} title="Zoom Out">
          <svg
            xmlns="http://www.w3.org/2000/svg"
            width="20"
            height="20"
            viewBox="0 0 24 24"
            fill="none"
            stroke="currentColor"
            strokeWidth="2"
            strokeLinecap="round"
            strokeLinejoin="round"
          >
            <circle cx="11" cy="11" r="8"></circle>
            <line x1="21" y1="21" x2="16.65" y2="16.65"></line>
            <line x1="8" y1="11" x2="14" y2="11"></line>
          </svg>
        </button>

        <button className="toolbar-button zoom-value" onClick={handleZoomReset}>
          {zoom}%
        </button>

        <button className="toolbar-button" onClick={handleZoomIn} title="Zoom In">
          <svg
            xmlns="http://www.w3.org/2000/svg"
            width="20"
            height="20"
            viewBox="0 0 24 24"
            fill="none"
            stroke="currentColor"
            strokeWidth="2"
            strokeLinecap="round"
            strokeLinejoin="round"
          >
            <circle cx="11" cy="11" r="8"></circle>
            <line x1="21" y1="21" x2="16.65" y2="16.65"></line>
            <line x1="11" y1="8" x2="11" y2="14"></line>
            <line x1="8" y1="11" x2="14" y2="11"></line>
          </svg>
        </button>
      </div>

      {selectedElement && (
        <div className="toolbar-section element-actions">
          <button className="toolbar-button" onClick={() => onDuplicate(selectedElement)} title="Duplicate">
            <svg
              xmlns="http://www.w3.org/2000/svg"
              width="20"
              height="20"
              viewBox="0 0 24 24"
              fill="none"
              stroke="currentColor"
              strokeWidth="2"
              strokeLinecap="round"
              strokeLinejoin="round"
            >
              <rect x="9" y="9" width="13" height="13" rx="2" ry="2"></rect>
              <path d="M5 15H4a2 2 0 0 1-2-2V4a2 2 0 0 1 2-2h9a2 2 0 0 1 2 2v1"></path>
            </svg>
          </button>

          <button className="toolbar-button" onClick={() => onDelete(selectedElement.id)} title="Delete">
            <svg
              xmlns="http://www.w3.org/2000/svg"
              width="20"
              height="20"
              viewBox="0 0 24 24"
              fill="none"
              stroke="currentColor"
              strokeWidth="2"
              strokeLinecap="round"
              strokeLinejoin="round"
            >
              <polyline points="3 6 5 6 21 6"></polyline>
              <path d="M19 6v14a2 2 0 0 1-2 2H7a2 2 0 0 1-2-2V6m3 0V4a2 2 0 0 1 2-2h4a2 2 0 0 1 2 2v2"></path>
            </svg>
          </button>
        </div>
      )}
    </div>
  )
}

export default EditorToolbar
